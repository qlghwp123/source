//단, 출력되는 숫자들(20, 123, 456)은 서식문자 %d를 이용하여 출력하도록 하자.
//제 이름은 홍길동입니다.
//제 나이는 20살이고요.
//제가 사는 곳의 번지수는 123-456번지 입니다.
