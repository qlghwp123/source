#include <stdio.h>

int main(void)
{
	printf("제 이름은 이동근입니다.\n");
	printf("제 나이는 %d살이고요.\n", 23);
	printf("제가 사는 곳의 번지수는 %d-%d번지 입니다.\n", 203, 3);

	return 0;
}